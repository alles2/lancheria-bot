    private void editComboBox() throws ClassNotFoundException, SQLException {
        //método para popular o combobox com as categorias
        jPanel1.setBorder(BorderFactory.createTitledBorder("Adicionar Produto"));
        model.Categoria cat = new model.Categoria();

        ResultSet dadosCat = cat.getCategorias();

        while (dadosCat.next()) {
            jComboBox1.addItem(dadosCat.getString("descricao"));
        }
    }


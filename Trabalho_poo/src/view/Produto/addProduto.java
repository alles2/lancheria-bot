    public void adicionarProduto() {
        model.Produto prod = new model.Produto();

        //verifica se o campo está vazio, se estiver, envia um aviso ao usuário
        if (jTextPane1.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Você deve preencher o campo com o nome do produto!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (jTextPane2.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Você deve preencher o campo com o preço do produto!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        //evitando bug caso o usuário digite o preço com vírgula
        if (jTextPane2.getText().contains(",")) {
            String textoCampo = jTextPane2.getText();
            String campoComPonto = textoCampo.replaceAll(",", ".");
            jTextPane2.setText(campoComPonto);
        }

        try {
            prod.addProduto(jTextPane1.getText(), (String) jComboBox1.getSelectedItem(), Double.parseDouble(jTextPane2.getText()));
            JOptionPane.showMessageDialog(null, "Produto adicionado com sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            jTextPane1.setText("");
            jTextPane2.setText("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }


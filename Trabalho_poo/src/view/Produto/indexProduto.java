    public void atualizaTela() {
        //método para atualizar a tabela com os produtos
        DefaultTableModel tabela = (DefaultTableModel) jTable1.getModel();
        try {
            tabela.setRowCount(0);

            model.Produto cat = new model.Produto();
            ResultSet dadosCat = cat.getProdutos();

            while (dadosCat.next()) {
                tabela.addRow(new Object[]{
                    dadosCat.getString("id"),
                    dadosCat.getString("Cat.descricao"),
                    dadosCat.getString("preco"),
                    dadosCat.getString("produto.descricao"),});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }


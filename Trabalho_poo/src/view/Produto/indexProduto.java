    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //método para excluir produtos
        model.Produto prod = new model.Produto();

        if (jTable1.getSelectedColumnCount() <= 0) {
            JOptionPane.showMessageDialog(null, "Você deve escolher o produto que deseja excluir", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            prod.deleteProduto((String) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            JOptionPane.showMessageDialog(null, "Produto removido com sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

            atualizaTela();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

